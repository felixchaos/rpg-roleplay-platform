-- v001 initial_schema — Rust 端基底 schema,与 Python rpg/platform_app/db/init.py 完全对齐。
--
-- 设计原则:
--   - Rust 端单机起服务时,这一份 migration 必须能从空库建出 Python 端 init_db
--     之后的等价 schema,后续 v002..v022 才能在此基础上 alter / extend。
--   - 历史上 v001 用了 uuid 主键 + 独立 _users / 简化 books 设计,与 Python 的
--     bigint 主键 + users / 完整 init.py 表完全不兼容;v004..v021 的 FK 引用
--     `users(id)` / `scripts(id)` / `game_saves(id)` 在 fresh Rust DB 上全部
--     dangling。Wave 5-A 重写本文件对齐 Python,后续 v019/v021/v022 的
--     `create table if not exists` 在新库上变成 no-op,但仍保留以支持
--     Python 共享库的渐进升级。
--   - 兼容性:1.0 期 acceptable wipe;老 Rust prod 库(如有)需要 reset DB。
--
-- 对照来源(Python):
--   - 基表 DDL:rpg/platform_app/db/init.py L57..L630
--   - 批量 alter (public_id / row_version):L631..L657
--   - 单点 alter (chapter_count / state_snapshot / ...):L658..L676
--   - 索引:L677..L716

