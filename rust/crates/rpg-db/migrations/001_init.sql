-- ────────────────────────────────────────────────────────────────
-- public_id + row_version 批量列(对应 Python L631..L657 的循环)
-- ────────────────────────────────────────────────────────────────

do $$
declare
  t text;
begin
  foreach t in array array[
    'users','scripts','game_saves','branch_nodes','branch_commits','branch_refs',
    'runtime_checkouts','assets','script_chapters','books','documents',
    'document_chunks','chapter_facts','character_cards','worldbook_entries',
    'game_sessions','messages','memories','worldline_variables',
    'worldline_projections','context_runs','model_entries'
  ]
  loop
    execute format('alter table %I add column if not exists public_id uuid not null default gen_random_uuid()', t);
    execute format('alter table %I add column if not exists row_version bigint not null default 1', t);
    execute format('create unique index if not exists idx_%s_public_id on %I(public_id)', t, t);
  end loop;
end $$;

