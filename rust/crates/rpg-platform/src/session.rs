/// 一条未过期 session。
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ActiveSession {
    pub token_prefix: String,
    pub user_id: i64,
    pub expires_at: DateTime<Utc>,
    #[serde(default)]
    pub created_at: Option<DateTime<Utc>>,
}

fn mask_token(token: &str) -> String {
    let mut iter = token.chars();
    let head: String = iter.by_ref().take(6).collect();
    format!("{head}...")
}

/// 列出用户当前所有未过期 session(token 仅暴露前 6 位前缀)。
pub async fn list_active_sessions(
    pool: &PgPool,
    user_id: i64,
) -> PlatformResult<Vec<ActiveSession>> {
    let rows = sqlx::query(
        "select token, user_id, expires_at, \
                (select created_at from sessions s2 where s2.token = sessions.token) as created_at \
           from sessions \
          where user_id = $1 and expires_at > now() \
          order by expires_at desc",
    )
    .bind(user_id)
    .fetch_all(pool)
    .await?;
    let mut out = Vec::with_capacity(rows.len());
    for r in rows {
        let token: String = r.try_get("token")?;
        out.push(ActiveSession {
            token_prefix: mask_token(&token),
            user_id: r.try_get::<i64, _>("user_id").unwrap_or(user_id),
            expires_at: r
                .try_get::<DateTime<Utc>, _>("expires_at")
                .unwrap_or_else(|_| Utc::now()),
            created_at: r.try_get::<Option<DateTime<Utc>>, _>("created_at").ok().flatten(),
        });
    }
    Ok(out)
}

/// 主动失活一条 session(按 token,需 owner)。
pub async fn revoke_session(pool: &PgPool, user_id: i64, token: &str) -> PlatformResult<bool> {
    if token.is_empty() {
        return Ok(false);
    }
    let res = sqlx::query("delete from sessions where token = $1 and user_id = $2")
        .bind(token)
        .bind(user_id)
        .execute(pool)
        .await?;
    Ok(res.rows_affected() > 0)
}

/// 失活某用户全部 token(改密 / 强制下线场景)。返回删除条数。
pub async fn revoke_all_user_sessions(pool: &PgPool, user_id: i64) -> PlatformResult<u64> {
    let res = sqlx::query("delete from sessions where user_id = $1")
        .bind(user_id)
        .execute(pool)
        .await?;
    Ok(res.rows_affected())
}

/// 清理已过期 session。返回删除条数。
pub async fn cleanup_expired_sessions(pool: &PgPool) -> PlatformResult<u64> {
    let res = sqlx::query("delete from sessions where expires_at <= now()")
        .execute(pool)
        .await?;
    Ok(res.rows_affected())
}
