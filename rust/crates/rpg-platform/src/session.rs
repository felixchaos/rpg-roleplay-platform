//! session —— Session 管理 (login_audit + active_sessions)。
//!
//! 对应 Python `rpg/platform_app/auth.py` 中 `sessions` 表 + `login_audit` 表
//! 操作的查询/审计部分(注册/登录主流程在 `auth/sessions.rs` 完成)。
//!
//! 提供:
//! - `record_login_audit` 写一条登录事件(login_ok/login_fail/admin_unlock)
//! - `list_login_audit` 翻审计日志(管理员视角)
//! - `list_active_sessions` 列出某用户当前未过期 token(去 password_hash)
//! - `revoke_session` / `revoke_all_user_sessions` 主动失活
//! - `cleanup_expired_sessions` 定期清理已过期 token
//!
//! 表 DDL `login_audit` 已在 rpg-db migrations 里;本模块兜底 CREATE IF NOT EXISTS。

use chrono::{DateTime, Utc};
use serde::{Deserialize, Serialize};
use serde_json::Value;
use sqlx::{PgPool, Row};

use crate::error::PlatformResult;

const AUDIT_DDL: &str = "create table if not exists login_audit (\
      id bigint generated by default as identity primary key,\
      username text,\
      ip text,\
      event text not null,\
      meta jsonb not null default '{}'::jsonb,\
      created_at timestamptz not null default now()\
    )";

const AUDIT_INDEX_DDL: &str =
    "create index if not exists idx_login_audit_user_time on login_audit(username, created_at desc)";
const AUDIT_EVENT_INDEX_DDL: &str =
    "create index if not exists idx_login_audit_event on login_audit(event, created_at desc)";

/// 兜底建 login_audit 表。
pub async fn init_login_audit_table(pool: &PgPool) -> PlatformResult<()> {
    sqlx::query(AUDIT_DDL).execute(pool).await?;
    sqlx::query(AUDIT_INDEX_DDL).execute(pool).await?;
    sqlx::query(AUDIT_EVENT_INDEX_DDL).execute(pool).await?;
    Ok(())
}

/// `login_audit` 一行。
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct LoginAuditRow {
    pub id: i64,
    pub username: Option<String>,
    pub ip: Option<String>,
    pub event: String,
    pub meta: Value,
    pub created_at: DateTime<Utc>,
}

fn row_to_audit(row: &sqlx::postgres::PgRow) -> sqlx::Result<LoginAuditRow> {
    Ok(LoginAuditRow {
        id: row.try_get("id")?,
        username: row.try_get::<Option<String>, _>("username").unwrap_or(None),
        ip: row.try_get::<Option<String>, _>("ip").unwrap_or(None),
        event: row.try_get::<String, _>("event").unwrap_or_default(),
        meta: row
            .try_get::<Value, _>("meta")
            .unwrap_or(Value::Object(Default::default())),
        created_at: row
            .try_get::<DateTime<Utc>, _>("created_at")
            .unwrap_or_else(|_| Utc::now()),
    })
}

/// 写一条审计。失败吞掉(不阻塞登录主流程,语义与 Python 一致)。
pub async fn record_login_audit(
    pool: &PgPool,
    username: &str,
    ip: &str,
    event: &str,
    meta: Value,
) {
    if init_login_audit_table(pool).await.is_err() {
        return;
    }
    let _ = sqlx::query(
        "insert into login_audit(username, ip, event, meta) values ($1, $2, $3, $4)",
    )
    .bind(if username.is_empty() {
        None
    } else {
        Some(username)
    })
    .bind(if ip.is_empty() { None } else { Some(ip) })
    .bind(event)
    .bind(&meta)
    .execute(pool)
    .await;
}

/// 翻审计日志(可按 username + event 过滤)。
pub async fn list_login_audit(
    pool: &PgPool,
    username: Option<&str>,
    event: Option<&str>,
    limit: i64,
) -> PlatformResult<Vec<LoginAuditRow>> {
    init_login_audit_table(pool).await?;
    let limit = limit.clamp(1, 500);
    let mut sql = String::from("select * from login_audit where 1=1");
    if username.is_some() {
        sql.push_str(" and username = $2");
    }
    if event.is_some() {
        if username.is_some() {
            sql.push_str(" and event = $3");
        } else {
            sql.push_str(" and event = $2");
        }
    }
    sql.push_str(" order by id desc limit $1");
    let mut q = sqlx::query(&sql).bind(limit);
    if let Some(u) = username {
        q = q.bind(u);
    }
    if let Some(e) = event {
        q = q.bind(e);
    }
    let rows = q.fetch_all(pool).await?;
    rows.iter()
        .map(row_to_audit)
        .collect::<Result<Vec<_>, _>>()
        .map_err(Into::into)
}

