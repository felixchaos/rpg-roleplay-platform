@router.get("/api/search")
async def api_search(request: Request):
    """Cross-resource search. Returns grouped results (scripts, saves, cards, chapters)."""
    user = require_user(request)
    q = (request.query_params.get("q") or "").strip()
    if not q:
        return json_response({"ok": True, "groups": []})
    pattern = f"%{q}%"
    init_db()
    groups = []
    with connect() as db:
        scripts = db.execute(
            "select id, title, description from scripts where owner_id = %s and title ilike %s limit 8",
            (user["id"], pattern),
        ).fetchall()
        if scripts:
            groups.append({"kind": "scripts", "items": [
                {"id": r["id"], "label": r["title"], "sub": r["description"], "href": "Platform.html#scripts"} for r in scripts
            ]})
        saves = db.execute(
            "select id, title from game_saves where user_id = %s and title ilike %s limit 8",
            (user["id"], pattern),
        ).fetchall()
        if saves:
            groups.append({"kind": "saves", "items": [
                {"id": r["id"], "label": r["title"], "href": "Platform.html#saves"} for r in saves
            ]})
        try:
            cards = db.execute(
                "select id, name from character_cards where owner_user_id = %s and name ilike %s limit 8",
                (user["id"], pattern),
            ).fetchall()
            if cards:
                groups.append({"kind": "cards", "items": [
                    {"id": r["id"], "label": r["name"], "href": "Platform.html#cards"} for r in cards
                ]})
        except Exception:
            pass
    return json_response({"ok": True, "groups": groups, "query": q})


