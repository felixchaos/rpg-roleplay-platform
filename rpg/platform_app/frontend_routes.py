@router.post("/api/auth/password")
async def api_change_password(request: Request):
    """Change current user's password (`current` -> `next`)."""
    user = require_user(request)
    body = await request.json()
    cur_pw = body.get("current") or ""
    next_pw = body.get("next") or ""
    if len(next_pw) < _auth.MIN_PASSWORD_LENGTH:
        return _bad(f"新密码至少 {_auth.MIN_PASSWORD_LENGTH} 位")
    init_db()
    with connect() as db:
        row = db.execute("select password_hash from users where id = %s", (user["id"],)).fetchone()
        if not row or not verify_password(cur_pw, row["password_hash"]):
            return _bad("当前密码错误")
        db.execute(
            "update users set password_hash = %s, row_version = row_version + 1, updated_at = now() where id = %s",
            (hash_password(next_pw), user["id"]),
        )
        # Optional: revoke all OTHER sessions (keep current cookie).
        token = request.cookies.get(SESSION_COOKIE)
        if token:
            db.execute("delete from sessions where user_id = %s and token <> %s", (user["id"], token))
    return json_response({"ok": True, "message": "密码已修改"})


@router.get("/api/auth/login-history")
async def api_login_history(request: Request):
    """Return last N login_audit rows for the current user. ?format=csv supported."""
    user = require_user(request)
    init_db()
    fmt = (request.query_params.get("format") or "").lower()
    limit = max(1, min(int(request.query_params.get("limit") or 50), 500))
    with connect() as db:
        db.execute(
            """
            create table if not exists login_audit (
              id bigint generated by default as identity primary key,
              username text, ip text, event text not null,
              meta jsonb not null default '{}'::jsonb,
              created_at timestamptz not null default now()
            )
            """
        )
        rows = db.execute(
            """
            select id, username, ip, event, meta, created_at
            from login_audit
            where username = %s
            order by created_at desc
            limit %s
            """,
            (user.get("username"), limit),
        ).fetchall()
    items = [
        {
            "id": r["id"],
            "at": r["created_at"].isoformat() if r["created_at"] else None,
            "ip": r["ip"],
            "user_agent": (r["meta"] or {}).get("ua") if isinstance(r["meta"], dict) else None,
            "result": "ok" if r["event"] == "login_ok" else "blocked",
            "event": r["event"],
        }
        for r in rows
    ]
    if fmt == "csv":
        buf = io.StringIO()
        w = csv.writer(buf)
        w.writerow(["at", "ip", "event", "result"])
        for i in items:
            w.writerow([i.get("at"), i.get("ip"), i.get("event"), i.get("result")])
        return StreamingResponse(
            io.BytesIO(buf.getvalue().encode("utf-8")),
            media_type="text/csv; charset=utf-8",
            headers={"Content-Disposition": 'attachment; filename="login-history.csv"'},
        )
    return json_response({"ok": True, "entries": items})


@router.get("/api/auth/sessions")
async def api_list_sessions(request: Request):
    user = require_user(request)
    init_db()
    cur_token = request.cookies.get(SESSION_COOKIE)
    with connect() as db:
        rows = db.execute(
            """
            select token, user_id, created_at, expires_at
            from sessions
            where user_id = %s and expires_at > now()
            order by created_at desc
            """,
            (user["id"],),
        ).fetchall()
    out = []
    for r in rows:
        token = r["token"]
        out.append({
            "id": token[-12:],  # truncated, never expose the full token
            "session_id": token[-12:],
            "created_at": r["created_at"].isoformat() if r["created_at"] else None,
            "expires_at": r["expires_at"].isoformat() if r["expires_at"] else None,
            "last_seen_at": r["created_at"].isoformat() if r["created_at"] else None,
            "current": token == cur_token,
        })
    return json_response({"ok": True, "sessions": out})


